# 변수의 종류는 크게 3가지
# 정수 숫자(intger) 변수 예) -1, 0, 1, 2, 3
# 실수 숫자(float) 변수 예) 1.1, 3.141597...
# 문자열(string) 변수 예) "문자"
# 불리언(boolean) 변수 예) True or False

입력1 = int(input())  # 3
입력2 = int(input())  # 4

# == 같다
# 7   -> "7"  -> "결과값: " + "7" == "결과값: 7"
print("결과값: " + str(입력1 + 입력2))

print("결과값: " + str(입력1 - 입력2))
