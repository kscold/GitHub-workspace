# 변수는 그릇

# 타입(형식)
# intger int 3 숫자형 변수 20억까지
# float 실수형 3.14
# string str "문자열" 문자열 변수
# boolean True 또는 False 참과 거짓을 표현하는 변수

# 입력 출력의 줄임말

int1 = int(input())
int2 = int(input())

# 보간법(변수를 문자열에 매칭시킨다)
print(f"결과는 {int1 + int2}")

# 파이썬에서는 같은 타입끼리 더할 수 있음(str 문자열 타입끼리 더한 것임)
print("결과는 " + str(int1 + int2))
