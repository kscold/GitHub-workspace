# 복합 대입 연산자
# 복합 대입 연산자를 사용하는 이유는 귀찮음(비효율적) 때문에 사용한다.
# +=
# -=
# *=
# /=
# //=
# **=
# %=

number = 2  # 변수를 선언(대입)
# number = number + 2  # 4
number += 3  # number = number + 2
print(number)  # 5

# number = number * 2
number *= 2  # number = number * 2

print(number)


