# 파이썬 내부에 정의 되어 있는 메서드(함수 혹은 예약어 혹은 명령어)

# abs(x) # x의 절대값 |100|
print(abs(-100))
# pow(x, y) # x를 y만큼 곱한 값 2**3 =8
print(pow(2, 3))

# max랑 min은 기억해주세용
# max()
print(max(1, 2, 3, 5, 3, 647, 457))
# min()
print(min(1, 2, 3, 5, 3, 647, 457))

# round()
print(round(3.14))  # //
print(round(4.678, 2))  # 4.68
